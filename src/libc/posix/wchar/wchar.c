static int dpmi_int(int nr, struct rmi_s *rmi) {
    union REGS regs;
    struct SREGS sregs;
    memset(&sregs, 0, sizeof sregs);
    memset(&regs, 0, sizeof regs);
    regs.w.ax = DPMI_INT;
    regs.h.bl = (uint8_t)nr;
    sregs.es = FP_SEG(rmi);
    regs.x.edi = FP_OFF(rmi);
    regs.x.cflag = 1;
    int386x(0x31, &regs, &regs, &sregs);
    return regs.x.cflag & 1;
}
